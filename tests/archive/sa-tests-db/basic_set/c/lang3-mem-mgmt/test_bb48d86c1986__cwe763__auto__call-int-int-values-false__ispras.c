#include <stdlib.h>

int callee(int par1, int par2) {
  if (par1 < par2)
    return 1;

  return -1;
}

void func(int param) {
  int array[37];
  int *pointer = NULL;

  int local_var = 97;

  pointer = array;

  if (callee(local_var, 172) > 0) {
    exit(0);
  }

  free(pointer);
}
