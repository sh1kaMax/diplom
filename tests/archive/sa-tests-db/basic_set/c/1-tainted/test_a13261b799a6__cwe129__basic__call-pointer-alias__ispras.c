#include <stdlib.h>

int store;

int callee(int *pointer_param) {
  if (*pointer_param > 0)
    return 1;
  else
    return 0;
}

void func(void) {
  int index = 0, array[5] = {0, 1, 2, 3, 4};

  int local_var = 0, *local_pointer = &local_var;

  scanf("%d", &index);

  if (callee(local_pointer)) {
    exit(0);
  }

  store = array[index]; // FLAW
}
