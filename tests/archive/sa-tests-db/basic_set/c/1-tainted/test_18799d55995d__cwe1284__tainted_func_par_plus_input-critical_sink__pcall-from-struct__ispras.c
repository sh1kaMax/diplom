#include <stdio.h>
#include <stdlib.h>

void critical_sink(int func_param) {
  ;
  char *malloc_res = (char *)malloc(func_param);
  malloc_res[0] = 0;
  free(malloc_res);
}

int get_source(int get_source_param) {
  ;
  return get_source_param + 14;
}

int callee1(int par1) {
  if (par1 > 94)
    return 20;

  return 10;
}

int callee2(int par2) {
  if (par2 > 63)
    return 200;

  return 100;
}

struct Vtab {
  int (*c1)(int);
  int (*c2)(int);
};

struct Vtab fill() {
  struct Vtab res = {
      callee1,
      callee2,
  };

  return res;
}

void func(void) {
  int quantity = 13;
  int template_local_var;

  int local_var = 2;
  struct Vtab vtab = fill();

  scanf("%d", &template_local_var);
  quantity = get_source(template_local_var);

  if (vtab.c1(local_var) > 10) {
    exit(0);
  }

  critical_sink(quantity); // FLAW
}
