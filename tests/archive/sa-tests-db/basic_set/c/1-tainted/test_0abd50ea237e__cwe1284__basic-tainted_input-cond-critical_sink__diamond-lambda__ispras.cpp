#include <stdio.h>
#include <stdlib.h>

void critical_sink(int func_param, int func_flag) {
  if (func_flag < 0)
    return;
  char *malloc_res = (char *)malloc(func_param);
  malloc_res[0] = 0;
  free(malloc_res);
}

void func(void) {
  int quantity = 11;
  int template_flag = 90;

  int local_var1 = 48;

  auto init = [&]() { local_var1 = 160; };

  if (local_var1 < 160) {
    scanf("%d", &quantity);
  }

  init();

  if (local_var1 < 160) {
    critical_sink(quantity, template_flag);
  }
}
