// случае истинности проверки.

#include <stdio.h>
#include <stdlib.h>

int external_func(void);

void critical_sink(int func_param) {
  char *malloc_res = (char *)malloc(func_param);
  malloc_res[0] = 0;
  free(malloc_res);
}

int get_source() {
  int get_source_var;
  scanf("%d", &get_source_var);
  return get_source_var;
}

void func(void) {
  int quantity = 14;

  quantity = get_source();

  if (external_func()) {

    quantity = 14;

    goto error_label;
  }

  exit(0);

error_label:
  critical_sink(quantity);
}
