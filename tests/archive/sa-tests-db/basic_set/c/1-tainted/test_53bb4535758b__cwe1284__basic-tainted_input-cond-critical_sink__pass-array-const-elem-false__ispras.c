#include <stdio.h>
#include <stdlib.h>

void critical_sink(int func_param, int func_flag) {
  if (func_flag < 0)
    return;
  char *malloc_res = (char *)malloc(func_param);
  malloc_res[0] = 0;
  free(malloc_res);
}

void func(int param) {
  int quantity = 10;
  int template_flag = 1;

  int arr[100];
  arr[29] = !param;
  arr[55] = 7;

  if (param) {
    scanf("%d", &quantity);
  }

  param = arr[29];

  if (param) {
    critical_sink(quantity, template_flag);
  }
}
