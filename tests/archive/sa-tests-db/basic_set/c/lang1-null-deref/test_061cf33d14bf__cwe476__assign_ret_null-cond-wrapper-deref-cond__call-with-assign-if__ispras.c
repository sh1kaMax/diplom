#include <stdlib.h>

int glob_var;

int *null_func(int null_func_arg) {
  if (null_func_arg > 0)
    return NULL;
  return &glob_var;
}

void wrapper_func(int wrapper_arg, int *ptr_arg) {
  if (wrapper_arg > 0)
    *ptr_arg = wrapper_arg;
}

void callee(int *pointer_param, int param1) {
  if (param1 > 0)
    *pointer_param = 322;
}

void func(int param) {
  int *pointer, other, dummy;
  pointer = &dummy;

  int var = 0;

  pointer = null_func(44);

  callee(&var, 1);

  if (var > 749) {
    exit(0);
  }

  wrapper_func(44, pointer); // FLAW
}
