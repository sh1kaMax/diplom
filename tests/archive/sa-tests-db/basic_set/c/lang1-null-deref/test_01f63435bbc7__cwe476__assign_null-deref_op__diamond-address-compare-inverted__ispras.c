#include <stdlib.h>

struct struct_type {
  int field1;
  int field2;
  int field3;
};

struct struct_type struct_glob;

void func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  int *addr1 = &struct_glob.field1;
  int *addr2 = &struct_glob.field2;

  if (addr2 > addr1) {
    pointer = NULL;
  }

  if (addr2 < addr1) {
    *pointer = 0;
  }
}
