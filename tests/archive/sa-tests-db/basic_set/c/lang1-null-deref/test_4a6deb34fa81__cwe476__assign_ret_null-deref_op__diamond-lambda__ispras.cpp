#include <stdlib.h>

int *null_func(int null_func_arg) { return NULL; }

void func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  int local_var1 = 2;

  auto init = [&]() { local_var1 = 124; };

  if (local_var1 > -7) {
    pointer = null_func(60);
  }

  init();

  if (local_var1 == 124) {
    *pointer = 0; // FLAW
  }
}
