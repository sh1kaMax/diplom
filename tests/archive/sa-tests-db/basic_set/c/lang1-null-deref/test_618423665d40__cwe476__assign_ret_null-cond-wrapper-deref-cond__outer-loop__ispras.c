#include <stdlib.h>

int glob_var;

int *null_func(int null_func_arg) {
  if (null_func_arg > 0)
    return NULL;
  return &glob_var;
}

void wrapper_func(int wrapper_arg, int *ptr_arg) {
  if (wrapper_arg > 0)
    *ptr_arg = wrapper_arg;
}

void func(void) {
  int iter_var;

  int *pointer, other, dummy;
  pointer = &dummy;

  for (iter_var = 5; iter_var < 28; iter_var++) {
    pointer = null_func(75);

    wrapper_func(75, pointer); // FLAW
  }
}
