// Поточный вариант: const-bool-func-ret-member.cpp
// Путь от источника до стока зависит от функции, которая всегда возвращает
// значение члена класса. Значение устанавливается вначале в true, затем в
// false.

#include <stdlib.h>

int *null_func(int null_func_arg) { return NULL; }

class SomeClass {
  bool member_flag;

public:
  bool isTrue();

  void func(void);
};

bool SomeClass::isTrue() { return member_flag; }

void SomeClass::func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  member_flag = true;

  pointer = null_func(48);

  member_flag = false;

  if (isTrue()) {
    *pointer = 0;
  }
}
