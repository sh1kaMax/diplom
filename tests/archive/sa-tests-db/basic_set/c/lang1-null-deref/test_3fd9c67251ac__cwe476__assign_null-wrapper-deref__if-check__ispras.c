#include <stdlib.h>

void wrapper(int *ptr) { *ptr = 1; }

void func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  pointer = NULL;

  if (pointer == NULL) {
    exit(0);
  }

  wrapper(pointer);
}
