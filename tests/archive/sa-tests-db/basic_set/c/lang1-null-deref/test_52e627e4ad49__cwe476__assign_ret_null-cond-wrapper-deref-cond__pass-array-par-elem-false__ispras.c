int glob_var;

int *null_func(int null_func_arg) {
  if (null_func_arg > 0)
    return NULL;
  return &glob_var;
}

void wrapper_func(int wrapper_arg, int *ptr_arg) {
  if (wrapper_arg > 0)
    *ptr_arg = wrapper_arg;
}

void func(int param) {
  int *pointer, other, dummy;
  pointer = &dummy;

  int arr[100];
  int some_var = 9;
  if (param < 0 || param > 99)
    exit(0);
  arr[param] = some_var;
  arr[60] = 0;

  if (some_var) {
    pointer = null_func(80);
  }

  if (arr[60]) {
    wrapper_func(80, pointer);
  }
}
