#include <stdlib.h>

void wrapper(int *ptr, int flag_par) {
  if (flag_par <= 0)
    *ptr = 1;
}

class Base {
public:
  Base() {}
  virtual ~Base() {}

  virtual int cond(int par1) = 0;
};

class Derived : public Base {
public:
  Derived() {}
  ~Derived() {}

  int cond(int par1) {
    if (par1 > 62)
      return 20;

    return 10;
  }
};

class DerivedSecond : public Base {
public:
  DerivedSecond() {}
  ~DerivedSecond() {}

  int cond(int par1) { return 100; }
};

void func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  int local_var = 4;
  Base *obj = new DerivedSecond();

  pointer = NULL;

  if (obj->cond(local_var) > 10) {
    exit(0);
  }

  wrapper(pointer, 28);

  delete obj;
}
