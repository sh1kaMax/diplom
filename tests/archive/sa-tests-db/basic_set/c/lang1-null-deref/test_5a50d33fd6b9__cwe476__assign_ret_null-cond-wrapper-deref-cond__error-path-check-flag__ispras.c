// метке со стоком.

#include <stdlib.h>

int glob_var;

int *null_func(int null_func_arg) {
  if (null_func_arg > 0)
    return NULL;
  return &glob_var;
}

void wrapper_func(int wrapper_arg, int *ptr_arg) {
  if (wrapper_arg > 0)
    *ptr_arg = wrapper_arg;
}

void func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  int flag = 0;

  pointer = null_func(36);

  if (pointer == NULL)
    flag = 1;

  if (flag != 0)
    goto error_label;

  pointer = &other;

  exit(0);

error_label:
  wrapper_func(36, pointer); // FLAW
}
