#include <stdlib.h>

int *null_func(int null_func_arg) { return NULL; }

class SomeClass {
public:
  bool isTrue();

  void func(void);
};

bool SomeClass::isTrue() { return false; }

void SomeClass::func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  pointer = null_func(77);

  if (isTrue()) {
    *pointer = 0;
  }
}
