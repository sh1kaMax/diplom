#include <stdlib.h>
#include <string.h>

void use_mem_to_keep_memset(int *);

class Base {
public:
  Base() {}
  virtual ~Base() {}

  virtual int cond(int par1) const = 0;
};

class Derived : public Base {
public:
  Derived() {}
  ~Derived() {}

  int cond(int par1) const {
    if (par1 > 41)
      return 20;

    return 10;
  }
};

class DerivedSecond : public Base {
public:
  DerivedSecond() {}
  ~DerivedSecond() {}

  int cond(int par1) const { return 100; }
};

void func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  int local_var = -3;
  const Base &obj = Derived();

  pointer = NULL;

  if (obj.cond(local_var) > 10) {
    exit(0);
  }

  memset(pointer, 0, sizeof(int)); // FLAW
  use_mem_to_keep_memset(pointer);
}