#include <limits.h>
#include <stdint.h>
#include <stdio.h>
#include <stdlib.h>

void func(void) {
  uint64_t data = 0;
  uint64_t result;

  data = ULLONG_MAX;

  if (data >= ULLONG_MAX) {
    data = 0;
  }

  result = data + 1;
}
