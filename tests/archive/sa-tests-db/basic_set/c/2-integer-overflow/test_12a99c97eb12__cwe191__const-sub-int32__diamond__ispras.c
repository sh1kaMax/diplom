#include <limits.h>
#include <stdint.h>
#include <stdio.h>
#include <stdlib.h>

void func(int param) {
  int32_t data = 1;
  int32_t result;

  if (param > 16) {
    data = INT_MIN;
  }

  if (param > 16) {
    result = data - 1; // FLAW
  }
}
