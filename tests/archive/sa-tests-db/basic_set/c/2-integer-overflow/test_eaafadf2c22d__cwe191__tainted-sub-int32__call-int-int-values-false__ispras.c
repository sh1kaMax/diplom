// завершается

#include <limits.h>
#include <stdint.h>
#include <stdio.h>
#include <stdlib.h>

int callee(int par1, int par2) {
  if (par1 < par2)
    return 1;

  return -1;
}

void func(int param) {
  int32_t data = 1;
  int32_t result;

  int local_var = 46;

  scanf("%d", &data);

  if (callee(local_var, 100) > 0) {
    exit(0);
  }

  result = data - 1;
}
