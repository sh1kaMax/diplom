#include <stdio.h>
#include <stdlib.h>
#include <string.h>

char *source_string;

void func(int param) {
  char string[30];
  int number = 29;

  if (param > 30) {
    scanf("%d", &number);
    if (number > 29)
      number = 29;
  }

  if (param > 30) {
    strncpy(string, source_string, number); // FLAW
    string[29] = '\0';
  }
}
