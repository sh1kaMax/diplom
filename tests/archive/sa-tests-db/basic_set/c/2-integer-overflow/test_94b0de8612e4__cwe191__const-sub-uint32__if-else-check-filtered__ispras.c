#include <limits.h>
#include <stdint.h>
#include <stdio.h>
#include <stdlib.h>

void func(void) {
  uint32_t data = 1;
  uint32_t result;

  data = 0;

  if (data <= 0) {
    data = 1;
  } else {
    result = data - 1;
  }
}
