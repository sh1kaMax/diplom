#include <stdio.h>
#include <stdlib.h>
#include <string.h>

const char safe_format[] = "Safe format string";

void func(void) {
  char format[38] = "Initial format string";

  scanf("%37s", format);

  if (strchr(format, '%') != 0) {
    strcpy(format, safe_format);
  }

  printf(format);
}
