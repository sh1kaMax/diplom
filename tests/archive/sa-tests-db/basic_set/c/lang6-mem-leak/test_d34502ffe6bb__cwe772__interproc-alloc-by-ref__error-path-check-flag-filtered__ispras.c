#include <stdio.h>
#include <stdlib.h>

void my_fopen(FILE **ptr, const char *my_path) {
  if (ptr != NULL) {
    *ptr = fopen(my_path, "r");
  }
}

void func(const char *path) {
  FILE *f = NULL;

  int flag = 0;

  my_fopen(&f, path); // FLAW

  if (f != NULL)
    flag = 1;

  if (flag != 0) {

    f = NULL;

    goto error_label;
  }

  exit(0);

error_label:
  if (f != NULL) {
    fclose(f);
  }
}
