// CWE: 772
// Название: Missing Release of Resource after Effective Lifetime
// Модельный вариант: interproc-no-free.json
//
// Утечка файлового дескриптора при выходе из области видимости переменной,
// хранящей открытый дескриптор, или перезаписи переменной перед вызовом
// fclose(). Дескриптор передается в пользовательскую функцию, которая не
// освобождает его.
//
// Поточный вариант: pass-array-const-elem.c
// Достижимый путь от источника до стока. И источник и сток находятся под
// условиями, значения которых проходят через элемент массива.

#include <stdio.h>
#include <stdlib.h>

void no_fclose(FILE *ptr) {
  if (ptr == NULL) {
    fclose(ptr);
  }
}

void func(int param, const char *path) {
  FILE *f = NULL;

  int arr[100];
  arr[48] = param;
  arr[71] = 7;

  if (param) {
    f = fopen(path, "r"); // FLAW
  }

  param = arr[48];

  if (param) {
    if (f != NULL) {
      no_fclose(f);
    }
  }
}
