// результата вызова функции.

#include <stdio.h>
#include <stdlib.h>

void my_malloc(void **ptr, size_t my_size) {
  if (ptr != NULL) {
    *ptr = malloc(my_size);
  }
}

int ret_func(int param1) { return param1; }

void func(int param, size_t size) {
  void *f = NULL;

  int func_res = ret_func(10);

  if (param + func_res > 13) {
    my_malloc(&f, size); // FLAW
  }

  if (param + func_res < 13) {
    if (f != NULL) {
      free(f);
    }
  }
}
