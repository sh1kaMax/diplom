void func(void) {
  int *buffer = (int *)malloc(37 * sizeof(int));
  int result = 0;
  unsigned int index = 0;

  int var;

  scanf("%u", &index);

  var = (index >= 37 ? 0 : 1);

  if (var) {
    result = buffer[index];
  }

  free(buffer);
  return;
}
