#include <stdlib.h>

int buffer[34];
int get_buffer_elem(int elem_index) { return buffer[elem_index]; }

class SomeClass {
  int member_flag;

public:
  bool isTrue(int is_true_par);

  void setVal(int set_val_par);

  void func(void);
};

bool SomeClass::isTrue(int is_true_par) { return member_flag > is_true_par; }

void SomeClass::setVal(int set_val_par) { member_flag = set_val_par; }

void SomeClass::func(void) {
  int result = 0;
  unsigned int index = 0;

  setVal(-52);

  index = 34;

  setVal(-40);

  if (isTrue(-50)) {
    setVal(6);

    result = get_buffer_elem(index); // FLAW
  }

  return;
}
