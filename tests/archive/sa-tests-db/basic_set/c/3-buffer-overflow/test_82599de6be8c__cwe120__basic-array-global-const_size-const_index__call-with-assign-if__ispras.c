// Модельный вариант: basic-array-global-const_size-const_index.json
//
// Чтение за правой границей буфера.
// Буфер выделен в статической памяти.
// Буфер является обычным массивом.
// Размер буфера является константой.
// Доступ к буферу осуществляется непосредственно (с помощью операции
// индексации). Индекс является константой.
//
// Поточный вариант: call-with-assign-if.c
// Достижимый путь от источника до стока с вызовом функции, которая присваивает
// значение через переданный указатель, если 2й параметр больше нуля. При вызове
// функции 2й параметр больше нуля, поэтому присвоение происходит. Присвоенное
// значение сравнивается с константой, проверка неуспешна и выполнение достигает
// стока.

#include <stdlib.h>

int buffer[25];

void callee(int *pointer_param, int param) {
  if (param > 0)
    *pointer_param = 107;
}

void func(void) {
  int result = 0;
  unsigned int index = 0;

  int var = 0;

  index = 25;

  callee(&var, 3);

  if (var > 697) {
    exit(0);
  }

  result = buffer[index]; // FLAW

  return;
}
