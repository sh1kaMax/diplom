#include <stdlib.h>

int buffer[22];

int func(unsigned int param) {
  int result = 0;
  unsigned int index = 0;

  int var;

  if (param > 0 && param < 14) {
    index = (param * 2 + 1);
  } else {
    exit(0);
    ;
  }

  var = (index >= 22 ? 1 : 0);

  if (var) {
    result = buffer[index]; // FLAW
  }

  return result;
}
