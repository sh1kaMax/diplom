#include <stdlib.h>

int result = 0;

class ExceptionClass {};

void func(void) {
  int buffer[31];
  unsigned int index = 0;

  try {
    result = buffer[index];
    throw ExceptionClass();
    index = 31;
  } catch (const ExceptionClass &excpt) {
  }

  return;
}
