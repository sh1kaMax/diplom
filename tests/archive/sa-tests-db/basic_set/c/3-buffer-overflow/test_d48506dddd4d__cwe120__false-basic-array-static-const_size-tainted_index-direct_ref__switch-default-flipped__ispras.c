#include <stdio.h>
#include <stdlib.h>

int buffer[25];

void func(void) {
  int result = 0;
  unsigned int index = 0;

  int var;

  scanf("%u", &index);

  var = 0;

  switch (var) {
  default:
    result = buffer[index];
    break;
  case 0:
    exit(0);
    break;
  }

  return;
}
