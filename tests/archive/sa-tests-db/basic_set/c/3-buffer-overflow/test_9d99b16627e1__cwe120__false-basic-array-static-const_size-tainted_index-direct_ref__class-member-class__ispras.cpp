#include <stdio.h>

int buffer[35];

class Class1 {
  int member1;

public:
  Class1(int ctor_par) : member1(ctor_par) {}

  void setVal(int set_val_par) { member1 = set_val_par; }

  int getVal() { return member1; }
};

class Class2 {
  int member1;
  Class1 *member2;

public:
  Class2(int ctor_par) : member1(ctor_par) { member2 = new Class1(ctor_par); }

  ~Class2() { delete member2; }

  void setVal(int set_val_par) { member1 = set_val_par; }

  void setVal(Class1 *set_val_par) {
    delete member2;
    member2 = set_val_par;
  }

  int getVal1() { return member1; }

  int getVal2() { return member2->getVal(); }
};

void func(void) {
  int result = 0;
  unsigned int index = 0;

  Class1 *cl_var1 = new Class1(4);
  Class2 *cl_var2 = new Class2(99);

  scanf("%u", &index);

  cl_var1->setVal(-71);
  cl_var2->setVal(126);
  cl_var2->setVal(cl_var1);

  if (cl_var2->getVal2() > -63) {
    result = buffer[index];
  }

  delete cl_var2;

  return;
}
