// Название: Buffer Overflow
// Модельный вариант: false-local-array-const_size-func-safe-access.json
//
// Чтение за правой границей буфера.
// Буфер выделен в автоматической памяти в вызываемой функции.
// Буфер является обычным массивом.
// Размер буфера является константой.
// Доступ к буферу осуществляется с помощью вызова функции, в которой этот буфер
// определён. В функции доступа есть проверка на границы индекса. Индекс
// является константой.
//
// Поточный вариант: virtual-call-0.cpp
// Путь от источника до стока зависит от возвращаемого значения метода,
// вызванного косвенно как метод базового класса

#include <stdlib.h>

int get_buffer_elem(int elem_index) {
  int buffer[37];
  int cur_index = 0;
  for (; cur_index < 37; cur_index++)
    buffer[cur_index] = 14;
  if (elem_index >= 37)
    return -1;
  return buffer[elem_index];
}

class Base {
public:
  Base() {}
  virtual ~Base() {}

  virtual int cond(int par1) = 0;
};

class Derived : public Base {
public:
  Derived() {}
  ~Derived() {}

  int cond(int par1) {
    if (par1 > 29)
      return 20;

    return 10;
  }
};

class DerivedSecond : public Base {
public:
  DerivedSecond() {}
  ~DerivedSecond() {}

  int cond(int par1) { return 100; }
};

void func(void) {
  int result = 0;
  unsigned int index = 0;

  int local_var = 2;
  Base *obj = new DerivedSecond();

  index = 37;

  if (obj->cond(local_var) > 10) {
    exit(0);
  }

  result = get_buffer_elem(index);

  delete obj;

  return;
}
