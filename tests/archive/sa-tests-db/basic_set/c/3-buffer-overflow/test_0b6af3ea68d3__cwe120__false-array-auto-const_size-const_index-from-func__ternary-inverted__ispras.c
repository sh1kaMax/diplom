// Чтение за правой границей буфера.
// Буфер выделен в автоматической памяти.
// Буфер является обычным массивом.
// Размер буфера является константой.
// Индекс получен за счёт вызова функции.
// Доступ к буферу осуществляется непосредственно (с помощью операции
// индексации). Индекс является константой.
//
// Поточный вариант: ternary-inverted.c
// Недостижимый путь от источника до стока с проверкой условия наличия
// уязвимости между ними с помощью тернарного оператора, выполнение не достигает
// стока в случае истинности проверки.

#include <stdlib.h>

int result = 0;

int index_change_func(int func_par) { return func_par + 2; }

void func(void) {
  int buffer[22];
  unsigned int index = 0;

  int var;

  index = 22;

  var = (index >= 22 ? 0 : 1);

  if (var) {
    index = index_change_func(index);
    result = buffer[index];
  }

  return;
}
