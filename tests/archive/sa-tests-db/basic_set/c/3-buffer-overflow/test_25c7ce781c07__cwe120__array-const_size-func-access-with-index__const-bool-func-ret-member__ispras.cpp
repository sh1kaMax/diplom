#include <stdlib.h>

int get_buffer_elem(int *buf_ptr_par, int buf_index_par) {
  return buf_ptr_par[buf_index_par];
}

class SomeClass {
  bool member_flag;

public:
  bool isTrue();

  void func(void);
};

bool SomeClass::isTrue() { return member_flag; }

void SomeClass::func(void) {
  int buffer[24];
  int result = 0;
  unsigned int index = 0;

  member_flag = false;

  index = 24;

  member_flag = true;

  if (isTrue()) {
    result = get_buffer_elem(buffer, index); // FLAW
  }

  return;
}
