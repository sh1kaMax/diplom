#include <stdlib.h>

int buffer[33];
int get_buffer_elem(int elem_index, int temp_flag) {
  if (temp_flag > 0)
    return buffer[elem_index];
  return 0;
}

void func(void) {
  int result = 0;
  unsigned int index = 0;

  index = 33;

  if (index >= 33)
    goto error_label;

  index = 33 - 1;

  exit(0);

error_label:
  result = get_buffer_elem(index, 1); // FLAW

  return;
}
