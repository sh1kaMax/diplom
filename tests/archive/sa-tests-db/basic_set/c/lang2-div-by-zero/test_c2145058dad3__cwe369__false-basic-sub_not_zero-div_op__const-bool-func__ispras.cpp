#include <stdlib.h>

class SomeClass {
public:
  bool isTrue();

  void func(void);
};

bool SomeClass::isTrue() { return false; }

void SomeClass::func(void) {
  int divident = 25, divisor = 7, result;
  int var_a = 121;
  int var_b = 130;

  divisor = var_a - var_b;

  if (isTrue()) {
    result = divident / divisor;
  }
}
