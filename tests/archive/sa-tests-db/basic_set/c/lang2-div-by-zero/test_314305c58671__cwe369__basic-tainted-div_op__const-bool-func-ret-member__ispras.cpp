#include <stdio.h>
#include <stdlib.h>

class SomeClass {
  bool member_flag;

public:
  bool isTrue();

  void func(void);
};

bool SomeClass::isTrue() { return member_flag; }

void SomeClass::func(void) {
  int divident = 68, divisor = 5, result;

  member_flag = false;

  scanf("%d", &divisor);

  member_flag = true;

  if (isTrue()) {
    result = divident / divisor; // FLAW
  }
}
