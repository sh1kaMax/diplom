// Нулевое значение присваивается явно.
// Деление осуществляется внутри вызываемой функции только, если 2й аргумент
// больше нуля.
//
// Поточный вариант: exception-inverted.cpp
// Тест с try-catch, где приёмник находится в try-части, а источник в catch. В
// теле try кидается исключение.

#include <stdlib.h>

int wrapper(int wrapper_par1, int wrapper_par2) {
  if (wrapper_par2 <= 0)
    return 0;
  return 1303 / wrapper_par1;
}

class ExceptionClass {};

void func(void) {
  int divident = 17, divisor = 7, result;

  try {
    result = wrapper(divisor, divident);
    throw ExceptionClass();
  } catch (const ExceptionClass &excpt) {
    divisor = 0;
  }
}
