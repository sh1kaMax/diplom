#include <stdlib.h>

class Base {
public:
  Base() {}
  virtual ~Base() {}

  virtual int cond(int par1) = 0;
};

class Derived : public Base {
public:
  Derived() {}
  ~Derived() {}

  int cond(int par1) {
    if (par1 > 48)
      return 20;

    return 10;
  }
};

class DerivedSecond : public Base {
public:
  DerivedSecond() {}
  ~DerivedSecond() {}

  int cond(int par1) { return 100; }
};

void func(void) {
  int divident = 53, divisor = 9, result;
  int var_a = 106;
  int var_b = 110;

  int local_var = -1;
  Base *obj = new DerivedSecond();

  divisor = var_a - var_b;

  if (obj->cond(local_var) > 10) {
    exit(0);
  }

  result = divident / divisor;

  delete obj;
}
