#include <stdlib.h>

int wrapper(int wrapper_par) { return 16; }

class Base {
public:
  Base() {}
  virtual ~Base() {}

  virtual int cond(int par1) = 0;
};

class Derived : public Base {
public:
  Derived() {}
  ~Derived() {}

  int cond(int par1) {
    if (par1 > 84)
      return 20;

    return 10;
  }
};

class DerivedSecond : public Base {
public:
  DerivedSecond() {}
  ~DerivedSecond() {}

  int cond(int par1) { return 100; }
};

void func(void) {
  int divident = 43, divisor = 7, result;

  int local_var = 3;
  Base *obj = new DerivedSecond();

  divisor = wrapper(16);
  divisor -= 16;

  if (obj->cond(local_var) > 10) {
    exit(0);
  }

  result = divident / divisor;

  delete obj;
}
