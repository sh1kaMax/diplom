#include <stdio.h>
#include <stdlib.h>

void func(void) {
  int divident = 43, divisor = 3, result;

  scanf("%d", &divisor);

  if (divisor == 0) {
    exit(0);
  } else {
    result = divident / divisor;
  }
}
