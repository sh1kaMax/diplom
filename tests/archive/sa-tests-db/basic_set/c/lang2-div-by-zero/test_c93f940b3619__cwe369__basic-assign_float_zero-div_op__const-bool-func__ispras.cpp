#include <stdlib.h>

class SomeClass {
public:
  bool isTrue();

  void func(void);
};

bool SomeClass::isTrue() { return true; }

void SomeClass::func(void) {
  float divident = 29.18, divisor = 32.71, result;

  divisor = 0.0;

  if (isTrue()) {
    result = divident / divisor; // FLAW
  }
}
