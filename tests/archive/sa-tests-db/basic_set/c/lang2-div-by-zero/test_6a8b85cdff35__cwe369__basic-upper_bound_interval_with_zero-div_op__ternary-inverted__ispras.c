#include <stdlib.h>

int func(int param) {
  int divident = 16, result;

  int var;

  if (param < 13) {
    divident = 57;
  }

  var = (param == 0 ? 0 : 1);

  if (var) {
    result = divident / param;
  }

  return result;
}
