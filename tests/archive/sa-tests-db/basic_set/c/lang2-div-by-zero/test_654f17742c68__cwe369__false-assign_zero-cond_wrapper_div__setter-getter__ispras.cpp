#include <stdlib.h>

int wrapper(int wrapper_par1, int wrapper_par2) {
  if (wrapper_par2 <= 0)
    return 1;
  return 1147 / wrapper_par1;
}

class SomeClass {
  int member_flag;

public:
  bool isTrue();

  void setVal(int set_val_par);

  void func(void);
};

bool SomeClass::isTrue() { return member_flag > 16; }

void SomeClass::setVal(int set_val_par) { member_flag = set_val_par; }

void SomeClass::func(void) {
  int divident = 24, divisor = 5, result;

  setVal(14);

  divisor = 0;

  setVal(35);

  if (isTrue()) {
    setVal(32);

    result = wrapper(divisor, -divident);
  }
}
