// выставляет.

#include <stdlib.h>

int wrapper(int wrapper_par1, int wrapper_par2) {
  if (wrapper_par2 <= 0)
    return 0;
  return 1643 / wrapper_par1;
}

class SomeClass {
  int member_flag;

public:
  bool isTrue();

  void setVal(int set_val_par);

  void func(void);
};

bool SomeClass::isTrue() { return member_flag > 20; }

void SomeClass::setVal(int set_val_par) { member_flag = set_val_par; }

void SomeClass::func(void) {
  int divident = 58, divisor = 6, result;

  setVal(5);

  divisor = 0;

  setVal(9);

  if (isTrue()) {
    setVal(17);

    result = wrapper(divisor, divident);
  }
}
