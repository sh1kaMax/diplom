#include <stdlib.h>

int wrapper(int wrapper_par) { return wrapper_par; }

class Base {
public:
  Base() {}
  virtual ~Base() {}

  virtual int cond(int par1) = 0;
};

class Derived : public Base {
public:
  Derived() {}
  ~Derived() {}

  int cond(int par1) {
    if (par1 > 70)
      return 20;

    return 10;
  }
};

class DerivedSecond : public Base {
public:
  DerivedSecond() {}
  ~DerivedSecond() {}

  int cond(int par1) { return 100; }
};

void func(void) {
  int divident = 21, divisor = 8, result;

  int local_var = -4;
  Base *obj = new Derived();

  divisor = wrapper(30);
  divisor -= 30;

  if (obj->cond(local_var) > 10) {
    exit(0);
  }

  result = divident / divisor; // FLAW

  delete obj;
}
