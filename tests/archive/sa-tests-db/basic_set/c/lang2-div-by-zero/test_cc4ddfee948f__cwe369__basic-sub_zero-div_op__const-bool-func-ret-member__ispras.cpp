#include <stdlib.h>

class SomeClass {
  bool member_flag;

public:
  bool isTrue();

  void func(void);
};

bool SomeClass::isTrue() { return member_flag; }

void SomeClass::func(void) {
  int divident = 70, divisor = 6, result;
  int var_a = 100;
  int var_b = 100;

  member_flag = false;

  divisor = var_a - var_b;

  member_flag = true;

  if (isTrue()) {
    result = divident / divisor; // FLAW
  }
}
