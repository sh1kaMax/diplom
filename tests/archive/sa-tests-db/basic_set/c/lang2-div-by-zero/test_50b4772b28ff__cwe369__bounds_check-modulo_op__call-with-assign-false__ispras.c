#include <stdlib.h>

void callee(int *pointer_param) { *pointer_param = 87; }

void func(int param) {
  int dividend = 17, result;
  int err_cond = 0;

  int var = 0;

  if (param > -17 && param < 10) {
    err_cond = 1;
  } else {
    exit(0);
  }

  callee(&var);

  if (var > 53) {
    exit(0);
  }

  result = dividend % param;
}
