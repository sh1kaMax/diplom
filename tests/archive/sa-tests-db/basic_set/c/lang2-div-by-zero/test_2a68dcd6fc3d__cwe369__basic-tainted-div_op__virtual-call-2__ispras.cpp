#include <stdio.h>
#include <stdlib.h>

class Base {
public:
  Base() {}
  virtual ~Base() {}

  virtual int cond(int par1) const = 0;
};

class Derived : public Base {
public:
  Derived() {}
  ~Derived() {}

  int cond(int par1) const { return 100; }
};

class DerivedSecond : public Base {
public:
  DerivedSecond() {}
  ~DerivedSecond() {}

  int cond(int par1) const {
    if (par1 > 53)
      return 20;

    return 10;
  }
};

class DerivedDerived : public Derived {
public:
  DerivedDerived() {}
  ~DerivedDerived() {}
};

void func(void) {
  int divident = 70, divisor = 7, result;

  int local_var = 2;
  const Base &obj = DerivedDerived();

  scanf("%d", &divisor);

  if (obj.cond(local_var) > 10) {
    exit(0);
  }

  result = divident / divisor;
}