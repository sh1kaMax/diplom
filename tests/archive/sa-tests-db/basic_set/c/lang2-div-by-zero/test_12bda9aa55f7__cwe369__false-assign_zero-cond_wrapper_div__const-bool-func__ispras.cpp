#include <stdlib.h>

int wrapper(int wrapper_par1, int wrapper_par2) {
  if (wrapper_par2 <= 0)
    return 1;
  return 1977 / wrapper_par1;
}

class SomeClass {
public:
  bool isTrue();

  void func(void);
};

bool SomeClass::isTrue() { return true; }

void SomeClass::func(void) {
  int divident = 29, divisor = 10, result;

  divisor = 0;

  if (isTrue()) {
    result = wrapper(divisor, -divident);
  }
}
