#include <stdio.h>
#include <stdlib.h>

void critical_sink(int func_param, int func_flag) {
  if (func_flag < 0)
    return;
  char *malloc_res = (char *)malloc(func_param);
  malloc_res[0] = 0;
  free(malloc_res);
}

int callee(int par1) {
  if (par1 > 93)
    return 20;

  return 10;
}

void func(void) {
  int quantity = 8;
  int template_flag = 67;

  int local_var = -5;

  scanf("%d", &quantity);

  if (callee(local_var) > 10) {
    exit(0);
  }

  critical_sink(quantity, template_flag); // FLAW
}
