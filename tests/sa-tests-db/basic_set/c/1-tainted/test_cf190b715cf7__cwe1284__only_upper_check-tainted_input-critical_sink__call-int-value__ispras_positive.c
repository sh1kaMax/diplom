#include <stdio.h>
#include <stdlib.h>

void critical_sink(int func_param) {
  ;
  char *malloc_res = (char *)malloc(func_param);
  malloc_res[0] = 0;
  free(malloc_res);
}

int callee(int par1) {
  if (par1 > 27)
    return 20;

  return 10;
}

void func(void) {
  int quantity = 18;

  int local_var = 4;

  scanf("%d", &quantity);
  if (quantity > 18) {
    exit(0);
    ;
  }

  if (callee(local_var) > 10) {
    exit(0);
  }

  critical_sink(quantity); // FLAW
}
