#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>

class SomeClass {
  int member_flag;

public:
  bool isTrue(int is_true_par);

  void setVal(int set_val_par);

  void func(void);
};

bool SomeClass::isTrue(int is_true_par) { return member_flag > is_true_par; }

void SomeClass::setVal(int set_val_par) { member_flag = set_val_par; }

void SomeClass::func(void) {
  char name[1024];

  setVal(15);

  scanf("%1023s", name);

  setVal(26);

  if (isTrue(17)) {
    setVal(-37);

    sethostname(name, sizeof(name)); // FLAW
  }
}
