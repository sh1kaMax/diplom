#include <stdio.h>
#include <stdlib.h>

int store;

int callee(int par1) {
  if (par1 > 57)
    return 20;

  return 10;
}

void func(void) {
  int index = 0, array[5] = {0, 1, 2, 3, 4};

  int local_var = -1;
  int (*func_ptr)(int) = &callee;

  scanf("%d", &index);

  if (func_ptr(local_var) == 10) {
    exit(0);
  }

  store = array[index];
}
