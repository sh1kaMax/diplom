#include <stdio.h>
#include <stdlib.h>

void critical_sink(int func_param) {
  char *malloc_res = (char *)malloc(func_param);
  malloc_res[0] = 0;
  free(malloc_res);
}

int get_source() {
  int get_source_var;
  scanf("%d", &get_source_var);
  return get_source_var;
}

void func(void) {
  int quantity = 10;

  int var;

  quantity = get_source();

  var = 0;

  switch (var) {
  case 0:
    exit(0);
  case 1:
    critical_sink(quantity);
    break;
  }
}
