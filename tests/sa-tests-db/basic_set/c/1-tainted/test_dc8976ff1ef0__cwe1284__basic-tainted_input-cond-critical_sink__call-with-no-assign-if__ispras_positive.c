// Количество получено от пользователя.
// Отсутствуют проверки выхода количества за пределы допустимых значений.
// Количество передаётся в функцию, которая вызывает malloc в зависимости от
// флага.
//
// Поточный вариант: call-with-no-assign-if.c
// Достижимый путь от источника до стока с вызовом функции, которая присваивает
// значение через переданный указатель, если 2й параметр больше нуля. При вызове
// функции 2й параметр больше нуля, поэтому присвоение не происходит.
// Присвоенное значение сравнивается с константой, проверка неуспешна и
// выполнение достигает стока.

#include <stdio.h>
#include <stdlib.h>

void critical_sink(int func_param, int func_flag) {
  if (func_flag < 0)
    return;
  char *malloc_res = (char *)malloc(func_param);
  malloc_res[0] = 0;
  free(malloc_res);
}

void callee(int *pointer_param, int param) {
  if (param > 0)
    *pointer_param = 211;
}

void func(void) {
  int quantity = 12;
  int template_flag = 14;

  int var = 0;

  scanf("%d", &quantity);

  callee(&var, -8);

  if (var > 220) {
    exit(0);
  }

  critical_sink(quantity, template_flag); // FLAW
}
