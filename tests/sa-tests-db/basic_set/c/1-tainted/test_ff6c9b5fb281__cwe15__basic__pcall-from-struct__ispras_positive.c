// Источник загрязненного значения - пользовательский ввод
// Ошибка реализуется путем передачи загрязненного значения в вызов функции
// sethostname. (Ошибка может устраняться путем проверки загрязненного значения
// на соответствие безопасному значению или переопредением его на безопасное
// значение.)
//
// Поточный вариант: pcall-from-struct.c
// Достижимый путь от источника до стока с проверкой возвращаемого значения
// функции, которое зависит от локальной переменной, переданной в функцию по
// значению; возвращаемое из вызываемой функции значение больше 20, если
// параметр больше константы ([10;20]), поэтому проверка в вызывающей функции
// неуспешна и выполнение достигает стока. Вызов функции осуществляется по
// указателю сохраняемому в структуре.

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>

int callee1(int par1) {
  if (par1 > 77)
    return 20;

  return 10;
}

int callee2(int par2) {
  if (par2 > 15)
    return 200;

  return 100;
}

struct Vtab {
  int (*c1)(int);
  int (*c2)(int);
};

struct Vtab fill() {
  struct Vtab res = {
      callee1,
      callee2,
  };

  return res;
}

void func(void) {
  char name[1024];

  int local_var = -4;
  struct Vtab vtab = fill();

  scanf("%1023s", name);

  if (vtab.c1(local_var) > 10) {
    exit(0);
  }

  sethostname(name, sizeof(name)); // FLAW
}
