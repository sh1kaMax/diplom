#include <stdio.h>
#include <stdlib.h>
#include <string.h>

char *source_string;

void func(int param) {
  char string[68];
  short number = 67;

  if (param > 28) {
    scanf("%hd", &number);
    if (number > 67)
      number = 67;
  }

  if (param < 46) {
    strncpy(string, source_string, number); // FLAW
    string[67] = '\0';
  }
}
