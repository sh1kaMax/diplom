#include <stdio.h>
#include <stdlib.h>
#include <string.h>

char *source_string;

void func(int param) {
  char string[88];
  short number = 87;

  scanf("%hd", &number);
  if (number > 87)
    number = 87;

  if (param > 6 && param < 23) {
    strncpy(string, source_string, number); // FLAW
    string[87] = '\0';
  }
}
