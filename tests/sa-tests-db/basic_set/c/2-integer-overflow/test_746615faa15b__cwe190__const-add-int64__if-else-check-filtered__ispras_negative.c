#include <limits.h>
#include <stdint.h>
#include <stdio.h>
#include <stdlib.h>

void func(void) {
  int64_t data = 0;
  int64_t result;

  data = LLONG_MAX;

  if (data >= LLONG_MAX) {
    data = 0;
  } else {
    result = data + 1;
  }
}
