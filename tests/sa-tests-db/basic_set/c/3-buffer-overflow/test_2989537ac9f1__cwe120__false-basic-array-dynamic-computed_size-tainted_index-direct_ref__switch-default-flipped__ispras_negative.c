#include <stdio.h>
#include <stdlib.h>

int func(unsigned int param) {
  int *buffer = (int *)malloc(param * sizeof(int));
  int result = 0;
  unsigned int index = 0;

  int var;

  scanf("%u", &index);

  var = 0;

  switch (var) {
  default:
    result = buffer[index];
    break;
  case 0:
    exit(0);
    break;
  }

  free(buffer);
  return result;
}
