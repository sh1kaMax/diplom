// истинности проверки.

#include <stdio.h>
#include <stdlib.h>

int external_func(void);

int result = 0;

void func(void) {
  unsigned int size;
  scanf("%u", &size);
  int *buffer = (int *)malloc(size * sizeof(int));
  unsigned int index = 0;

  index = 26;

  if (external_func())
    goto error_label;

  index = size - 1;

  exit(0);

error_label:
  result = buffer[index]; // FLAW

  free(buffer);
  return;
}
