// случае истинности проверки

#include <stdio.h>
#include <stdlib.h>

int func(unsigned int param) {
  int *buffer = (int *)malloc(param * sizeof(int));
  int result = 0;
  unsigned int index = 0;

  int var;

  scanf("%u", &index);

  var = (index >= param ? 1 : 0);

  if (var) {
    result = buffer[index]; // FLAW
  }

  free(buffer);
  return result;
}
