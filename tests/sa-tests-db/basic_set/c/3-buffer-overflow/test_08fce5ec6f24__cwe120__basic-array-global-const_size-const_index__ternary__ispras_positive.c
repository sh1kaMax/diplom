// случае истинности проверки.

#include <stdlib.h>

int buffer[23];

void func(void) {
  int result = 0;
  unsigned int index = 0;

  int var;

  index = 23;

  var = (index >= 23 ? 1 : 0);

  if (var) {
    result = buffer[index]; // FLAW
  }

  return;
}
