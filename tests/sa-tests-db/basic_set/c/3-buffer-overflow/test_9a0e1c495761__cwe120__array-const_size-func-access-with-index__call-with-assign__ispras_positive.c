#include <stdlib.h>

int get_buffer_elem(int *buf_ptr_par, int buf_index_par) {
  return buf_ptr_par[buf_index_par];
}

void callee(int *pointer_param) { *pointer_param = 398; }

void func(void) {
  int buffer[27];
  int result = 0;
  unsigned int index = 0;

  int var = 0;

  index = 27;

  callee(&var);

  if (var > 599) {
    exit(0);
  }

  result = get_buffer_elem(buffer, index); // FLAW

  return;
}
