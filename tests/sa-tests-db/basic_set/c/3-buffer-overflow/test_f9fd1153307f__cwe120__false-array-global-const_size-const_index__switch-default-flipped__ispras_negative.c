#include <stdlib.h>

int buffer[26];

void func(void) {
  int result = 0;
  unsigned int index = 0;

  int var;

  index = 26;

  var = 0;

  switch (var) {
  default:
    result = buffer[index];
    break;
  case 0:
    exit(0);
    break;
  }

  return;
}
