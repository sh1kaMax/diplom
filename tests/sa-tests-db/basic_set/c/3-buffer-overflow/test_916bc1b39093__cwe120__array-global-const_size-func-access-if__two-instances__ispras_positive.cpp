#include <stdlib.h>

int buffer[36];
int get_buffer_elem(int elem_index, int temp_flag) {
  if (temp_flag > 0)
    return buffer[elem_index];
  return 0;
}

class SomeClass {
  int member1;

public:
  SomeClass(int ctor_par) : member1(ctor_par) {}

  int getVal() { return member1; }
};

void func(void) {
  int result = 0;
  unsigned int index = 0;

  SomeClass *cl_var1 = new SomeClass(17);
  SomeClass *cl_var2 = new SomeClass(-64);

  index = 36;

  if (cl_var1->getVal() > 15) {
    result = get_buffer_elem(index, 1); // FLAW
  }

  delete cl_var1;
  delete cl_var2;

  return;
}
