#include <stdio.h>
#include <stdlib.h>

FILE *my_fopen(const char *my_path) {
  if (my_path != NULL) {
    return fopen(my_path, "r");
  } else {
    return NULL;
  }
}

int callee(int par1, int par2) {
  if (par1 < par2)
    return 1;

  return -1;
}

void func(const char *path) {
  FILE *f = NULL;

  int local_var = 90;

  f = my_fopen(path);

  if (callee(local_var, 162) > 0) {
    exit(0);
  }

  if (f != NULL) {
    fclose(f);
  }
}
