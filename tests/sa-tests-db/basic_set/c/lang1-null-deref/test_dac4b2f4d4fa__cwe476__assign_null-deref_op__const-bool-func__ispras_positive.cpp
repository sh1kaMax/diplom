#include <stdlib.h>

class SomeClass {
public:
  bool isTrue();

  void func(void);
};

bool SomeClass::isTrue() { return true; }

void SomeClass::func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  pointer = NULL;

  if (isTrue()) {
    *pointer = 0; // FLAW
  }
}
