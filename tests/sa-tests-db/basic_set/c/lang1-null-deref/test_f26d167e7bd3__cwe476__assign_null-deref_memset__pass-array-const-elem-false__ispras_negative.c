#include <stdlib.h>
#include <string.h>

void use_mem_to_keep_memset(int *);

void func(int param) {
  int *pointer, other, dummy;
  pointer = &dummy;

  int arr[100];
  arr[32] = !param;
  arr[75] = 7;

  if (param) {
    pointer = NULL;
  }

  param = arr[32];

  if (param) {
    memset(pointer, 0, sizeof(int));
    use_mem_to_keep_memset(pointer);
  }
}
