#include <stdlib.h>

int *null_func(int null_func_arg) { return NULL; }

class Class1 {
  int member1;

public:
  Class1(int ctor_par) : member1(ctor_par) {}

  void setVal(int set_val_par) { member1 = set_val_par; }

  int getVal() { return member1; }
};

class Class2 {
  int member1;
  Class1 *member2;

public:
  Class2(int ctor_par) : member1(ctor_par) { member2 = new Class1(ctor_par); }

  ~Class2() { delete member2; }

  void setVal(int set_val_par) { member1 = set_val_par; }

  void setVal(Class1 *set_val_par) {
    delete member2;
    member2 = set_val_par;
  }

  int getVal1() { return member1; }

  int getVal2() { return member2->getVal(); }
};

void func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  Class1 *cl_var1 = new Class1(55);
  Class2 *cl_var2 = new Class2(46);

  pointer = null_func(95);

  cl_var1->setVal(-43);
  cl_var2->setVal(159);
  cl_var2->setVal(cl_var1);

  if (cl_var2->getVal2() > -33) {
    *pointer = 0;
  }

  delete cl_var2;
}
