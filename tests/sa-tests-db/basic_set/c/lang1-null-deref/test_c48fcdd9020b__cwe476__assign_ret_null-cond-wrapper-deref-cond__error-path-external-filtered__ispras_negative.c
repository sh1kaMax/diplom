// случае истинности проверки.

#include <stdlib.h>

int external_func(void);

int glob_var;

int *null_func(int null_func_arg) {
  if (null_func_arg > 0)
    return NULL;
  return &glob_var;
}

void wrapper_func(int wrapper_arg, int *ptr_arg) {
  if (wrapper_arg > 0)
    *ptr_arg = wrapper_arg;
}

void func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  pointer = null_func(67);

  if (external_func()) {

    pointer = &other;

    goto error_label;
  }

  exit(0);

error_label:
  wrapper_func(67, pointer);
}
