#include <stdlib.h>

int glob_var;

int *null_func(int null_func_arg) {
  if (null_func_arg > 0)
    return NULL;
  return &glob_var;
}

void wrapper_func(int wrapper_arg, int *ptr_arg) {
  if (wrapper_arg > 0)
    *ptr_arg = wrapper_arg;
}

void func(int param) {
  int *pointer, other, dummy;
  pointer = &dummy;

  int arr[100];
  arr[31] = !param;
  arr[69] = 7;

  if (param) {
    pointer = null_func(78);
  }

  param = arr[31];

  if (param) {
    wrapper_func(78, pointer);
  }
}
