#include <stdlib.h>

int glob_var;

int *null_func(int null_func_arg) {
  if (null_func_arg > 0)
    return NULL;
  return &glob_var;
}

void wrapper_func(int wrapper_arg, int *ptr_arg) {
  if (wrapper_arg > 0)
    *ptr_arg = wrapper_arg;
}

void func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  int local_var1 = 7;

  auto init = [&]() { local_var1 = 155; };

  if (local_var1 > 4) {
    pointer = null_func(7);
  }

  init();

  if (local_var1 == 155) {
    wrapper_func(7, pointer); // FLAW
  }
}
