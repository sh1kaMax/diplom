// ноль в зависимости от параметра. Разыменование указателя осуществляется с
// помощью вызова функции, которая разыменовывает указатель под условием.
//
// Поточный вариант: two-instances.cpp
// Тест содержит 2 экземпляра одного класса. Путь от источника до стока зависит
// от вызова функции одного из экземпляров.

#include <stdlib.h>

int glob_var;

int *null_func(int null_func_arg) {
  if (null_func_arg > 0)
    return NULL;
  return &glob_var;
}

void wrapper_func(int wrapper_arg, int *ptr_arg) {
  if (wrapper_arg > 0)
    *ptr_arg = wrapper_arg;
}

class SomeClass {
  int member1;

public:
  SomeClass(int ctor_par) : member1(ctor_par) {}

  int getVal() { return member1; }
};

void func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  SomeClass *cl_var1 = new SomeClass(74);
  SomeClass *cl_var2 = new SomeClass(-64);

  pointer = null_func(9);

  if (cl_var2->getVal() > -61) {
    wrapper_func(9, pointer);
  }

  delete cl_var1;
  delete cl_var2;
}
