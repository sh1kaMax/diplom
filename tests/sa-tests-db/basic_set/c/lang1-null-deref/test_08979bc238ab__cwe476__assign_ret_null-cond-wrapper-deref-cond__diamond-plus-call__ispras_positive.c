#include <stdlib.h>

int glob_var;

int *null_func(int null_func_arg) {
  if (null_func_arg > 0)
    return NULL;
  return &glob_var;
}

void wrapper_func(int wrapper_arg, int *ptr_arg) {
  if (wrapper_arg > 0)
    *ptr_arg = wrapper_arg;
}

int ret_func(int param1) { return param1; }

void func(int param) {
  int *pointer, other, dummy;
  pointer = &dummy;

  int func_res = ret_func(10);

  if (param + func_res > 37) {
    pointer = null_func(50);
  }

  if (param + func_res > 37) {
    wrapper_func(50, pointer); // FLAW
  }
}
