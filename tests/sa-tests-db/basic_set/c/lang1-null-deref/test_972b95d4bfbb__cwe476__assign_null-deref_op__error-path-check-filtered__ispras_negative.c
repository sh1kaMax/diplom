#include <stdlib.h>

void func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  pointer = NULL;

  if (pointer == NULL) {

    pointer = &other;

    goto error_label;
  }

  exit(0);

error_label:
  *pointer = 0;
}
