#include <stdlib.h>

int *null_func(int null_func_arg) { return NULL; }

class Base {
public:
  Base() {}
  virtual ~Base() {}

  virtual int cond(int par1) const = 0;
};

class Derived : public Base {
public:
  Derived() {}
  ~Derived() {}

  int cond(int par1) const {
    if (par1 > 28)
      return 20;

    return 10;
  }
};

class DerivedSecond : public Base {
public:
  DerivedSecond() {}
  ~DerivedSecond() {}

  int cond(int par1) const { return 100; }
};

void func(void) {
  int *pointer, other, dummy;
  pointer = &dummy;

  int local_var = 2;
  const Base &obj = Derived();

  pointer = null_func(85);

  if (obj.cond(local_var) > 10) {
    exit(0);
  }

  *pointer = 0; // FLAW
}