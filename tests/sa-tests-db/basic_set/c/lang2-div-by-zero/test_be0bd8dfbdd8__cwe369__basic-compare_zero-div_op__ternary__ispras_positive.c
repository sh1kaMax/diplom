#include <stdlib.h>

int func(int param) {
  int divident = 16, result;

  int var;

  if (param == 0) {
    divident = 60;
  }

  var = (param == 0 ? 1 : 0);

  if (var) {
    result = divident / param; // FLAW
  }

  return result;
}
