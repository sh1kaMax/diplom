#include <stdlib.h>

void callee(int *pointer_param) { *pointer_param = 138; }

void func(int param) {
  int divident = 28, divisor = 4, result;

  int var = 0;

  divisor = 0;

  callee(&var);

  if (var > 110) {
    exit(0);
  }

  result = divident / divisor;
}
