#include <stdlib.h>

int wrapper(int wrapper_par1, int wrapper_par2) {
  if (wrapper_par2 <= 0)
    return 0;
  return 1252 / wrapper_par1;
}

void func(void) {
  int divident = 20, divisor = 5, result;

  divisor = 0;

  if (divisor == 0) {
    exit(0);
  }

  result = wrapper(divisor, divident);
}
