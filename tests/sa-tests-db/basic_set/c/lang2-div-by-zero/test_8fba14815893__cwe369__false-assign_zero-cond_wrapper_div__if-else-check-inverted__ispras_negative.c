#include <stdlib.h>

int wrapper(int wrapper_par1, int wrapper_par2) {
  if (wrapper_par2 <= 0)
    return 1;
  return 1179 / wrapper_par1;
}

void func(void) {
  int divident = 61, divisor = 10, result;

  divisor = 0;

  if (!(divisor == 0)) {
    exit(0);
  } else {
    result = wrapper(divisor, -divident);
  }
}
