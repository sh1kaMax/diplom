#include <stdlib.h>

int wrapper(int wrapper_par) { return 1047 / (wrapper_par - 19); }

struct struct_type {
  int field1;
  int field2;
  int field3;
};

int callee(struct struct_type *struct_param) {
  if (struct_param->field2 > 20)
    return 1;
  else
    return 0;
}

void func(void) {
  int divident = 18, divisor = 10, result;

  struct struct_type struct_var;
  struct_var.field1 = 9;
  struct_var.field2 = 2;
  struct_var.field3 = 54;

  divisor = 19;

  if (callee(&struct_var)) {
    exit(0);
  }

  result = wrapper(divisor); // FLAW
}
