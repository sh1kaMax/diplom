int wrapper(int wrapper_par) { return 1062 / (wrapper_par - 10); }

class SomeClass {
  bool member_flag;

public:
  bool isTrue();

  void func(void);
};

bool SomeClass::isTrue() { return member_flag; }

void SomeClass::func(void) {
  int divident = 58, divisor = 8, result;

  member_flag = false;

  divisor = 28;

  member_flag = true;

  if (isTrue()) {
    result = wrapper(divisor);
  }
}
