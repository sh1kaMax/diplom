#include <stdio.h>
#include <stdlib.h>

int external_func(void);

void func(void) {
  int divident = 59, divisor = 5, result;

  scanf("%d", &divisor);

  if (external_func())
    goto error_label;

  divisor = 59;

  exit(0);

error_label:
  result = divident / divisor; // FLAW
}
