#include <stdlib.h>

int wrapper(int wrapper_par1, int wrapper_par2) {
  if (wrapper_par2 <= 0)
    return 0;
  return 1952 / wrapper_par1;
}

int callee(int *pointer_param) {
  if (*pointer_param >= 0)
    return 1;
  else
    return 0;
}

void func(void) {
  int divident = 61, divisor = 8, result;

  int var = 0;

  divisor = 0;

  if (callee(&var)) {
    exit(0);
  }

  result = wrapper(divisor, divident);
}
