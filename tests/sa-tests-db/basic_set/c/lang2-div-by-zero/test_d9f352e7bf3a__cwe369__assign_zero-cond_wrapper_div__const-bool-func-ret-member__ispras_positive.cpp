#include <stdlib.h>

int wrapper(int wrapper_par1, int wrapper_par2) {
  if (wrapper_par2 <= 0)
    return 0;
  return 1811 / wrapper_par1;
}

class SomeClass {
  bool member_flag;

public:
  bool isTrue();

  void func(void);
};

bool SomeClass::isTrue() { return member_flag; }

void SomeClass::func(void) {
  int divident = 57, divisor = 3, result;

  member_flag = false;

  divisor = 0;

  member_flag = true;

  if (isTrue()) {
    result = wrapper(divisor, divident); // FLAW
  }
}
