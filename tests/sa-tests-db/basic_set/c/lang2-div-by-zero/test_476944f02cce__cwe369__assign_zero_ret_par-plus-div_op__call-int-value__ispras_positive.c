#include <stdlib.h>

int wrapper(int wrapper_par) { return wrapper_par; }

int callee(int par1) {
  if (par1 > 31)
    return 20;

  return 10;
}

void func(int param) {
  int divident = 27, divisor = 7, result;

  int local_var = 5;

  divisor = wrapper(31);
  divisor -= 31;

  if (callee(local_var) > 10) {
    exit(0);
  }

  result = divident / divisor; // FLAW
}
