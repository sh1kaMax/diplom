#include <stdlib.h>

int wrapper(int wrapper_par) { return 81; }

class Base {
public:
  Base() {}
  virtual ~Base() {}

  virtual int cond(int par1) const = 0;
};

class Derived : public Base {
public:
  Derived() {}
  ~Derived() {}

  int cond(int par1) const {
    if (par1 > 19)
      return 20;

    return 10;
  }
};

class DerivedSecond : public Base {
public:
  DerivedSecond() {}
  ~DerivedSecond() {}

  int cond(int par1) const { return 100; }
};

void func(void) {
  int divident = 21, divisor = 7, result;

  int local_var = 1;
  const Base &obj = Derived();

  divisor = wrapper(27);
  divisor -= 71;

  if (obj.cond(local_var) > 10) {
    exit(0);
  }

  result = divident / divisor;
}