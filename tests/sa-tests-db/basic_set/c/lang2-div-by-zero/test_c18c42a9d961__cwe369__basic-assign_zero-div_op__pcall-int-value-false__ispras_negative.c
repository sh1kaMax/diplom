#include <stdlib.h>

int callee(int par1) {
  if (par1 > 47)
    return 20;

  return 10;
}

void func(void) {
  int divident = 66, divisor = 8, result;

  int local_var = -1;
  int (*func_ptr)(int) = &callee;

  divisor = 0;

  if (func_ptr(local_var) == 10) {
    exit(0);
  }

  result = divident / divisor;
}
