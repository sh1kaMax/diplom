#include <stdlib.h>

int wrapper(int wrapper_par1, int wrapper_par2) {
  if (wrapper_par2 <= 0)
    return 1;
  return 1936 / wrapper_par1;
}

void func(void) {
  int divident = 44, divisor = 3, result;

  divisor = 0;

  if (divisor == 0)
    goto error_label;

  divisor = 3;

  exit(0);

error_label:
  result = wrapper(divisor, -divident);
}
