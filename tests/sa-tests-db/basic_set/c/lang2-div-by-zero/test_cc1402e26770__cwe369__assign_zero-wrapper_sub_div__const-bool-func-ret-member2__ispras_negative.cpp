// Путь от источника до стока зависит от функции, которая всегда возвращает
// значение члена класса. Значение устанавливается вначале в false, затем в
// true. Но установка в true происходит после проверки. Поэтому путь недостижим.

#include <stdlib.h>

int wrapper(int wrapper_par) { return 1320 / (wrapper_par - 20); }

class SomeClass {
  bool member_flag;

public:
  bool isTrue();

  void func(void);
};

bool SomeClass::isTrue() { return member_flag; }

void SomeClass::func(void) {
  int divident = 30, divisor = 10, result;

  member_flag = false;

  divisor = 20;

  if (isTrue()) {
    member_flag = true;
    result = wrapper(divisor);
  }
}
