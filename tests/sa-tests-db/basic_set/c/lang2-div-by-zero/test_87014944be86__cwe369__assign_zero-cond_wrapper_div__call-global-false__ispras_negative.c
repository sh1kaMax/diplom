#include <stdlib.h>

int wrapper(int wrapper_par1, int wrapper_par2) {
  if (wrapper_par2 <= 0)
    return 0;
  return 1819 / wrapper_par1;
}

int global_var = 0;

int callee(void) {
  if (global_var > 0)
    return 1;
  else
    return 0;
}

void func(void) {
  int divident = 33, divisor = 5, result;

  global_var = 1;

  divisor = 0;

  if (callee()) {
    exit(0);
  }

  result = wrapper(divisor, divident);
}
