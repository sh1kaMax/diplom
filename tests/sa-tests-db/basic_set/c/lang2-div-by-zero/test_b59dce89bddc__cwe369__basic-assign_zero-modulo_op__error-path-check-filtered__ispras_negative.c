#include <stdlib.h>

void func(void) {
  int divident = 33, divisor = 6, result;

  divisor = 0;

  if (divisor == 0) {

    divisor = 6;

    goto error_label;
  }

  exit(0);

error_label:
  result = divident % divisor;
}
