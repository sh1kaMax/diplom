#include <stdlib.h>

int wrapper(int wrapper_par) { return 37; }

int callee(int *pointer_param) {
  if (*pointer_param > 0)
    return 1;
  else
    return 0;
}

void func(void) {
  int divident = 17, divisor = 8, result;

  int var = 0;

  divisor = wrapper(15);
  divisor -= 37;

  if (callee(&var)) {
    exit(0);
  }

  result = divident / divisor; // FLAW
}
