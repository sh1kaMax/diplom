#include <stdlib.h>

class SomeClass {
  bool member_flag;

public:
  bool isTrue();

  void func(void);
};

bool SomeClass::isTrue() { return member_flag; }

void SomeClass::func(void) {
  float divident = 9.35, divisor = 43.23, result;

  member_flag = false;

  divisor = 0.0;

  member_flag = true;

  if (isTrue()) {
    result = divident / divisor; // FLAW
  }
}
