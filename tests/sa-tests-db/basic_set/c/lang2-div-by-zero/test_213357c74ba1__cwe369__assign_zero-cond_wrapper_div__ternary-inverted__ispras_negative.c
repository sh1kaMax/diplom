#include <stdlib.h>

int wrapper(int wrapper_par1, int wrapper_par2) {
  if (wrapper_par2 <= 0)
    return 0;
  return 1984 / wrapper_par1;
}

void func(void) {
  int divident = 63, divisor = 6, result;

  int var;

  divisor = 0;

  var = (divisor == 0 ? 0 : 1);

  if (var) {
    result = wrapper(divisor, divident);
  }
}
