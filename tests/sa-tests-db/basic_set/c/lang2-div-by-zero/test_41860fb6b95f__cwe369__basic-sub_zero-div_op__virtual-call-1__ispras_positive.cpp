#include <stdlib.h>

class Base {
public:
  Base() {}
  virtual ~Base() {}

  virtual int cond(int par1) = 0;
};

class Derived : public Base {
public:
  Derived() {}
  ~Derived() {}

  int cond(int par1) {
    if (par1 > 57)
      return 20;

    return 10;
  }
};

class DerivedSecond : public Base {
public:
  DerivedSecond() {}
  ~DerivedSecond() {}

  int cond(int par1) { return 100; }
};

class DerivedDerived : public Derived {
public:
  DerivedDerived() {}
  ~DerivedDerived() {}
};

void func(void) {
  int divident = 25, divisor = 3, result;
  int var_a = 105;
  int var_b = 105;

  int local_var = -2;
  Base *obj = new DerivedDerived();

  divisor = var_a - var_b;

  if (obj->cond(local_var) > 10) {
    exit(0);
  }

  result = divident / divisor; // FLAW

  delete obj;
}
