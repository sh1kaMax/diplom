#include <stdlib.h>

int callee(int *array_param) {
  if (array_param[1] > 0)
    return 1;
  else
    return 0;
}

void func(void) {
  int divident = 57, divisor = 3, result;
  int var_a = 70;
  int var_b = 75;

  int local_array[5] = {-1, 0, 1, 2, 3};

  divisor = var_a - var_b;

  if (callee(local_array)) {
    exit(0);
  }

  result = divident / divisor;
}
