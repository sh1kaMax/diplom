#include <stdlib.h>

int callee(int *pointer_param) {
  if (*pointer_param >= 0)
    return 1;
  else
    return 0;
}

void func(void) {
  int divident = 20, divisor = 6, result;
  int var_a = 67;
  int var_b = 74;

  int var = 0;

  divisor = var_a - var_b;

  if (callee(&var)) {
    exit(0);
  }

  result = divident / divisor;
}
