#include <stdlib.h>

int wrapper(int wrapper_par1, int wrapper_par2) {
  if (wrapper_par2 <= 0)
    return 0;
  return 1378 / wrapper_par1;
}

int callee1(int par1) {
  if (par1 > 73)
    return 20;

  return 10;
}

int callee2(int par2) {
  if (par2 > 91)
    return 200;

  return 100;
}

struct Vtab {
  int (*c1)(int);
  int (*c2)(int);
};

struct Vtab fill() {
  struct Vtab res = {
      callee1,
      callee2,
  };

  return res;
}

void func(void) {
  int divident = 38, divisor = 8, result;

  int local_var = 5;
  struct Vtab vtab = fill();

  divisor = 0;

  if (vtab.c1(local_var) > 10) {
    exit(0);
  }

  result = wrapper(divisor, divident); // FLAW
}
