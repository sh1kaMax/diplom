#include <stdlib.h>

void func(int param) {
  int array[28];
  int *pointer = NULL;

  int arr[100];
  arr[19] = param;
  arr[64] = 7;

  if (param) {
    pointer = array;
  }

  param = arr[19];

  if (param) {
    free(pointer); // FLAW
  }
}
