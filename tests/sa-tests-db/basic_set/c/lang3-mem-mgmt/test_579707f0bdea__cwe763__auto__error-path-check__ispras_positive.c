#include <stdlib.h>

void func(void) {
  int array[46];
  int *pointer = NULL;

  pointer = array;

  if (pointer == array)
    goto error_label;

  pointer = NULL;

  exit(0);

error_label:
  free(pointer); // FLAW
}
