#include <stdlib.h>

int external_func(void);

void func(void) {
  int array[29];
  int *pointer = NULL;

  pointer = array;

  if (external_func())
    goto error_label;

  pointer = NULL;

  exit(0);

error_label:
  free(pointer); // FLAW
}
